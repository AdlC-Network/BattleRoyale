package es.outlook.adriansrj.battleroyale.enums;

/**
 * Enumerates the different types of buses.
 *
 * @author AdrianSR / 03/09/2021 / 12:03 p. m.
 */
public enum EnumBusType {
	
	/**
	 * The bus that carries all the players in
	 * the arena. This is the bus that is used
	 * when the others are disabled.
	 */
	DRAGON,
	
	/**
	 * Pet as bus.
	 */
	PET,
	
	/**
	 * Vehicle as bus.
	 */
	VEHICLE;
}